# 出租车收费
# 3公里内收费13
# 超过3公里基本单价2.3元/公里
# 空驶费：超过15公里每公里加收基本单价的50%（3.45）
# 要求：输入公里 打印金额 以元为单位四舍五入

#方法1
n=input("请输入公里数:")
print("您的公里数是：",n)
n=int(n)
if 0 <= n <= 3:
	print("收费",13,"元")
elif 3<n<=15:
	print("收费",round(13+2.3*(n-3)),"元")
elif n>15:
	print("收费",round(13+2.3*(n-3)+1.15*(n-15)),"元")
else :
	print ("错误")


#方法2
n=input("请输入公里数:")
print("您的公里数是：",n)
fee=13
if 3<n:
	fee +=2.3*(n-3)
if 15<n:
	fee += 1.15 * (n - 15)
	print("收费", round(fee), "元")